#include <sys/ioctl.h>
#include <asm/termbits.h>
#include <asm/ioctls.h>

int force_baudrate(int fd, int speed)
{
	struct termios2 tio;
	ioctl(fd, TCGETS2, &tio);
	tio.c_cflag &= ~CBAUD;
	tio.c_cflag |= BOTHER;
	tio.c_ispeed = speed;
	tio.c_ospeed = speed;
	return ioctl(fd, TCSETS2, &tio);
}
